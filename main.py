                        new['변경유형'] = '신규 추가'
                        new['기존개수'] = '-'
                        new['個数_신규'] = new['個数'] # 신규 추가 항목도 비교를 위해 복사
                        
                        # 2. 개수 변경 추출
                        merged = pd.merge(df2, df1[['品番', '個数']], on='品番', suffixes=('_신규', '_기존'))

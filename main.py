import pandas as pd
from google.colab import files
import io

print("1. [기준 파일] 첫 번째 엑셀 파일을 업로드하세요.")
uploaded1 = files.upload()
file1_name = list(uploaded1.keys())[0]

print("\n2. [비교 대상] 두 번째 엑셀 파일을 업로드하세요.")
uploaded2 = files.upload()
file2_name = list(uploaded2.keys())[0]

xl1 = pd.ExcelFile(io.BytesIO(uploaded1[file1_name]))
xl2 = pd.ExcelFile(io.BytesIO(uploaded2[file2_name]))

sheets1 = xl1.sheet_names
sheets2 = xl2.sheet_names
output_name = "excel_comparison_all_sheets.xlsx"

with pd.ExcelWriter(output_name, engine='openpyxl') as writer:
    
    for sheet in sheets1:
        if sheet not in sheets2:
            print(f"⚠️ 경고: 두 번째 파일에 '{sheet}' 시트가 존재하지 않아 건너뜁니다.")
            continue
            
        print(f"\n➔ '{sheet}' 시트 분석 중...")
        
        # 💡 [보완 1] 만약 진짜 제목 줄이 1행이 아니라면 header=0 부분을 1이나 2로 고쳐보세요.
        df1 = xl1.parse(sheet, header=0)
        df2 = xl2.parse(sheet, header=0)
        
        # 💡 [보완 2] 제목 좌우에 있는 눈에 안 보이는 공백을 문자열에서 전부 제거합니다.
        df1.columns = [str(col).strip() for col in df1.columns]
        df2.columns = [str(col).strip() for col in df2.columns]
        
        quantity_col = '個数' 
        required_cols = ['品番', quantity_col]
        
        # 에러 발생 시 원인 분석을 위해 현재 엑셀의 제목들을 출력해줍니다.
        if not all(col in df1.columns for col in required_cols) or not all(col in df2.columns for col in required_cols):
            print(f"❌ '{sheet}' 시트 건너뜀: '品番' 또는 '{quantity_col}' 열을 찾을 수 없습니다.")
            print(f"   [확인 정보] 현재 엑셀 시트의 실제 제목들: {list(df1.columns)[:5]}... (앞쪽 5개만 표시)")
            
            # 빈 시트 생성
            pd.DataFrame(columns=['결과']).to_excel(writer, sheet_name=sheet, index=False)
            continue
            
        # --- 1. 신규 추가 품번 추출 ---
        new_pinban = df2[~df2['品番'].isin(df1['品番'])].copy()
        new_pinban['변경유형'] = '신규 추가'
        new_pinban['기존개수'] = '-'
        
        # --- 2. 개수(個数) 변경 품번 추출 ---
        merged = pd.merge(df2, df1[['品番', quantity_col]], on='品番', suffixes=('_신규', '_기존'))
        qty_changed = merged[merged[f'{quantity_col}_신규'] != merged[f'{quantity_col}_기존']].copy()
        
        qty_changed['변경유형'] = '개수 변경'
        qty_changed[quantity_col] = qty_changed[f'{quantity_col}_신규']
        qty_changed['기존개수'] = qty_changed[f'{quantity_col}_기존']
        qty_changed = qty_changed.drop(columns=[f'{quantity_col}_신규', f'{quantity_col}_기존'])
        
        # --- 3. 두 결과 합치기 ---
        final_result = pd.concat([new_pinban, qty_changed], ignore_index=True)
        
        print(f"   ✅ 분석 성공! (신규 추가: {len(new_pinban)}개 / 개수 변경: {len(qty_changed)}개)")
        final_result.to_excel(writer, sheet_name=sheet, index=False)

print(f"\n3. 모든 시트의 비교가 완료되었습니다! 결과 파일({output_name})을 다운로드합니다...")
files.download(output_name)

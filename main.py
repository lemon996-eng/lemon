Python
import pandas as pd
from google.colab import files
import io

print("1. [기준 파일] 첫 번째 엑셀 파일을 업로드하세요.")
uploaded1 = files.upload()
file1_name = list(uploaded1.keys())[0]

print("\n2. [비교 대상] 두 번째 엑셀 파일을 업로드하세요.")
uploaded2 = files.upload()
file2_name = list(uploaded2.keys())[0]

# 데이터 불러오기
df1 = pd.read_excel(io.BytesIO(uploaded1[file1_name]))
df2 = pd.read_excel(io.BytesIO(uploaded2[file2_name]))

# 엑셀의 수량 컬럼 이름을 입력하세요 (예: '数量', '수량', 'QTY' 등)
quantity_col = '数量' 

# 에러 방지: 필요한 열이 존재하는지 확인
required_cols = ['品番', quantity_col]
if not all(col in df1.columns for col in required_cols) or not all(col in df2.columns for col in required_cols):
    print(f"\n❌ 에러: 파일에 '品番' 또는 '{quantity_col}' 열이 존재하지 않습니다.")
    print("엑셀 파일의 맨 윗줄 제목을 확인해주세요.")
else:
    # --- 1. 첫 번째 파일에 아예 없는 새로운 품번 추출 ---
    new_pinban = df2[~df2['品番'].isin(df1['品番'])].copy()
    new_pinban['변경유형'] = '신규 추가'
    
    # --- 2. 품번은 같지만 수량이 변경된 데이터 추출 ---
    # 두 파일을 '品番' 기준으로 매칭(Merge)합니다.
    merged = pd.merge(df2, df1[['品番', quantity_col]], on='品番', suffixes=('_신규', '_기존'))
    
    # 기존 수량과 신규 수량이 다른 데이터만 골라냅니다.
    qty_changed = merged[merged[f'{quantity_col}_신규'] != merged[f'{quantity_col}_기존']].copy()
    
    # 보기 좋게 열 이름 정리 및 변경유형 기록
    qty_changed['변경유형'] = '수량 변경'
    qty_changed[quantity_col] = qty_changed[f'{quantity_col}_신규']
    qty_changed['기존수량'] = qty_changed[f'{quantity_col}_기존']
    
    # 필요없는 임시 열 삭제
    qty_changed = qty_changed.drop(columns=[f'{quantity_col}_신규', f'{quantity_col}_기존'])
    
    # --- 3. 두 결과 합치기 ---
    final_result = pd.concat([new_pinban, qty_changed], ignore_index=True)
    
    print(f"\n[분석 완료] 총 {len(final_result)}개의 변동 사항이 발견되었습니다.")
    print(f"- 신규 추가 품번: {len(new_pinban)}개")
    print(f"- 수량 변경 품번: {len(qty_changed)}개")
    
    # 결과 파일 생성 및 다운로드
    output_name = "excel_comparison_final.xlsx"
    final_result.to_excel(output_name, index=False)
    print(f"\n3. 결과 파일({output_name})을 다운로드합니다...")
    files.download(output_name)

                        # 문자열에 'Unnamed'가 포함되어 있거나 공백인 열 필터링
                        invalid_cols = [c for c in final.columns if 'Unnamed:' in str(c) or str(c).strip() == '']
                        final = final.drop(columns=invalid_cols)
                        
